def render_debug_html(engine_run: EngineRun) -> str:
    """Return the full HTML string for ``receipts/debug.html``.

    Pure function. No I/O. The caller writes the file.
    """
    abstain = engine_run.abstain
    abstain_state = abstain.state.value if (abstain is not None and abstain.state is not None) else "?"
    # S13.6-T1a (Option D): ``Abstain.reason`` stripped per Pivot 2.
    abstain_reason = None
    scale = engine_run.scale
    scale_lines: List[str] = []
    if scale is not None:
        if scale.monthly_revenue is not None:
            scale_lines.append(f"monthly_revenue={_fmt_money(scale.monthly_revenue)}")
        if scale.customer_base_est is not None:
            scale_lines.append(f"customer_base_est={_fmt_int(scale.customer_base_est)}")
        if scale.materiality_floor is not None:
            scale_lines.append(f"materiality_floor={_fmt_money(scale.materiality_floor)}")
    scale_block = ", ".join(scale_lines) or "—"

    # ---- Header / state-of-store --------------------------------------------
    head = f"""<!doctype html>
<html lang='en'>
<head>
<meta charset='utf-8'>
<title>BeaconAI internal debug — {_esc(engine_run.store_id or 'unknown')}</title>
<style>{_CSS}</style>
</head>
<body>
<h1>BeaconAI internal diagnostics</h1>
<div class='dbg-banner'>{_esc(INTERNAL_BANNER)}</div>
<section>
<h2>Run identity</h2>
<table>
<tr><th>store_id</th><td><code>{_esc(engine_run.store_id)}</code></td></tr>
<tr><th>run_id</th><td><code>{_esc(engine_run.run_id)}</code></td></tr>
<tr><th>anchor_date</th><td><code>{_esc(engine_run.anchor_date)}</code></td></tr>
<tr><th>schema_version</th><td><code>{_esc(engine_run.schema_version)}</code></td></tr>
<tr><th>cold_start</th><td><code>{_esc(engine_run.cold_start)}</code></td></tr>
<tr><th>decision_state</th><td><code>{_esc(abstain_state)}</code></td></tr>
<tr><th>abstain_reason</th><td>{_esc(abstain_reason or '')}</td></tr>
<tr><th>scale</th><td>{_esc(scale_block)}</td></tr>
<tr><th>data_quality_flags</th><td>{_render_data_quality_flags(engine_run.data_quality_flags)}</td></tr>
</table>
</section>
"""

    # ---- Recommended ---------------------------------------------------------
    rec_rows = "".join(_render_play_row(c) for c in (engine_run.recommendations or []))
    if not rec_rows:
        rec_rows = "<tr><td colspan='6'><em>(no recommendations on this run)</em></td></tr>"
    recommended = f"""<section>
<h2>Recommended ({len(engine_run.recommendations or [])})</h2>
<table>
<thead><tr>
<th>play_id</th>
<th>evidence_class</th>
<th>confidence_label</th>
<th>audience</th>
<th>measurement (p_internal / ci_internal)</th>
<th>revenue_range (drivers)</th>
</tr></thead>
<tbody>{rec_rows}</tbody>
</table>
</section>
"""

    # ---- Considered / rejected ----------------------------------------------
    rej_rows = "".join(_render_rejected_row(r) for r in (engine_run.considered or []))
    if not rej_rows:
        rej_rows = "<tr><td colspan='2'><em>(no considered/rejected entries on this run)</em></td></tr>"
    considered = f"""<section>
<h2>Considered, not recommended ({len(engine_run.considered or [])})</h2>
<table>
<thead><tr>
<th>play_id</th>
<th>reason_code</th>
</tr></thead>
<tbody>{rej_rows}</tbody>
</table>
</section>
"""

    # ---- Watching ------------------------------------------------------------
    watching_rows: List[str] = []
    for w in engine_run.watching or []:
        watching_rows.append(
            "<tr>"
            f"<td><code>{_esc(w.metric)}</code></td>"
            f"<td>{_esc(w.current)}</td>"
            f"<td>{_esc(w.prior)}</td>"
            f"<td>{_esc(w.trend)}</td>"
            f"<td>{_esc(w.threshold_to_act or '')}</td>"
            "</tr>"
        )
    if not watching_rows:
        watching_rows.append("<tr><td colspan='5'><em>(none)</em></td></tr>")
    watching = f"""<section>
<h2>Watching ({len(engine_run.watching or [])})</h2>
<table>
<thead><tr><th>metric</th><th>current</th><th>prior</th><th>trend</th><th>threshold_to_act</th></tr></thead>
<tbody>{''.join(watching_rows)}</tbody>
</table>
</section>
"""

    foot = "</body></html>"
    return head + recommended + considered + watching + foot


