@pytest.fixture(scope="module")
def scenarios():
    """Load scenario metadata from YAML."""
    with open(SCENARIOS_YAML) as f:
        data = yaml.safe_load(f)
    return data["scenarios"]


def _load_orders(scenario_name: str) -> pd.DataFrame:
    path = FIXTURES_DIR / f"{scenario_name}_orders.csv"
    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        df = pd.read_csv(path, low_memory=False)
    return df


def _load_inventory(scenario_name: str) -> pd.DataFrame:
    path = FIXTURES_DIR / f"{scenario_name}_inventory.csv"
    df = pd.read_csv(path, low_memory=False)
    return df


