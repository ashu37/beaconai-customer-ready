# Test: Promo anomaly has spike
# ---------------------------------------------------------------------------

class TestPromoAnomaly:
    def test_promo_month_has_higher_revenue(self, scenarios):
        cfg = scenarios["promo_anomaly_240d"]
        promo_month_idx = cfg.get("promo_month_index", 4)

        df = _load_orders("promo_anomaly_240d")
        paid = df[df["Financial Status"].astype(str).str.lower() == "paid"].copy()
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            paid["_date"] = pd.to_datetime(paid["Created at"], errors="coerce", utc=True).dt.tz_localize(None)
            paid_dedup = paid.drop_duplicates("Name")
            paid_dedup = paid_dedup.copy()
            paid_dedup["_month"] = paid_dedup["_date"].dt.to_period("M")
            monthly = paid_dedup.groupby("_month")["Subtotal"].sum()

        if len(monthly) <= promo_month_idx:
            pytest.skip("Not enough months to check promo spike")

        monthly_list = list(monthly)
        promo_rev = monthly_list[promo_month_idx]
        baseline_months = [r for i, r in enumerate(monthly_list) if i != promo_month_idx and r > 0]
        baseline_avg = sum(baseline_months) / len(baseline_months) if baseline_months else 1

        assert promo_rev >= baseline_avg * 1.5, (
            f"Promo month revenue ${promo_rev:,.0f} should be >= 1.5x baseline ${baseline_avg:,.0f}"
        )

    def test_promo_month_has_high_discounts(self, scenarios):
        cfg = scenarios["promo_anomaly_240d"]
        promo_month_idx = cfg.get("promo_month_index", 4)
        expected_discount_pct = cfg.get("promo_discount_pct", 0.35)

        df = _load_orders("promo_anomaly_240d")
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            df["_date"] = pd.to_datetime(df["Created at"], errors="coerce", utc=True).dt.tz_localize(None)
            df["_month"] = df["_date"].dt.to_period("M")

        months = sorted(df["_month"].dropna().unique())
        if len(months) <= promo_month_idx:
            pytest.skip("Not enough months")

        promo_month = months[promo_month_idx]
        promo_data = df[df["_month"] == promo_month].copy()
        promo_data["_discount"] = pd.to_numeric(promo_data["Total Discount"], errors="coerce")
        promo_data["_subtotal"] = pd.to_numeric(promo_data["Subtotal"], errors="coerce")
        promo_dedup = promo_data.drop_duplicates("Name")
        promo_dedup = promo_dedup[promo_dedup["_subtotal"] > 0]
        if promo_dedup.empty:
            pytest.skip("No paid promo month orders")

        avg_discount_pct = (promo_dedup["_discount"] / promo_dedup["_subtotal"]).mean()
        assert avg_discount_pct >= 0.15, (
            f"Promo month average discount {avg_discount_pct:.1%} should be >= 15%"
        )


