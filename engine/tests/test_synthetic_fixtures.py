# Test: Date range within spec
# ---------------------------------------------------------------------------

class TestDateRange:
    @pytest.mark.parametrize("scenario_name", SCENARIO_NAMES)
    def test_date_range_covers_required_window(self, scenario_name, scenarios):
        cfg = scenarios[scenario_name]
        anchor = pd.Timestamp(cfg["anchor_date"])
        days_history = cfg["days_history"]

        df = _load_orders(scenario_name)
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            dates = pd.to_datetime(df["Created at"], errors="coerce", utc=True).dt.tz_localize(None)

        actual_earliest = dates.min()
        actual_latest = dates.max()

        assert not pd.isna(actual_earliest), f"[{scenario_name}] No valid dates found"

        # Latest date should not be more than 2 days after anchor
        assert actual_latest <= anchor + pd.Timedelta(days=2), (
            f"[{scenario_name}] Latest date {actual_latest.date()} is after anchor {anchor.date()}"
        )

        # For 240d scenarios: data should go back at least 210 days
        if days_history >= 210:
            cutoff = anchor - pd.Timedelta(days=210)
            assert actual_earliest <= cutoff, (
                f"[{scenario_name}] Earliest date {actual_earliest.date()} > expected cutoff "
                f"anchor-210d = {cutoff.date()}"
            )

    @pytest.mark.parametrize("scenario_name", ["cold_start_45d"])
    def test_cold_start_date_range(self, scenario_name, scenarios):
        """Cold start should have < 60 days of data."""
        cfg = scenarios[scenario_name]
        anchor = pd.Timestamp(cfg["anchor_date"])

        df = _load_orders(scenario_name)
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            dates = pd.to_datetime(df["Created at"], errors="coerce", utc=True).dt.tz_localize(None)

        days_span = (dates.max() - dates.min()).days
        assert days_span <= 60, (
            f"[{scenario_name}] Data spans {days_span} days, expected <= 60"
        )


# ---------------------------------------------------------------------------
# Test: Monthly revenue within expected bounds
# ---------------------------------------------------------------------------

class TestMonthlyRevenue:
    @pytest.mark.parametrize(
        "scenario_name",
        [n for n in SCENARIO_NAMES if n not in ("cold_start_45d",)],
    )
    def test_monthly_revenue_in_range(self, scenario_name, scenarios):
        cfg = scenarios[scenario_name]
        rev_min = cfg.get("expected_monthly_revenue_min")
        rev_max = cfg.get("expected_monthly_revenue_max")

        if rev_min is None or rev_max is None:
            pytest.skip(f"No revenue bounds defined for {scenario_name}")

        df = _load_orders(scenario_name)
        paid = df[df["Financial Status"].astype(str).str.lower() == "paid"].copy()
        if paid.empty:
            pytest.skip(f"No paid orders in {scenario_name}")

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            paid["_date"] = pd.to_datetime(paid["Created at"], errors="coerce", utc=True).dt.tz_localize(None)
            paid_dedup = paid.drop_duplicates("Name")
            paid_dedup = paid_dedup.copy()
            paid_dedup["_month"] = paid_dedup["_date"].dt.to_period("M")
            monthly = paid_dedup.groupby("_month")["Subtotal"].sum()

        # Only check middle months (skip first and last partial months)
        if len(monthly) > 2:
            mid_months = monthly.iloc[1:-1]
            out_of_range = []
            for m, rev in mid_months.items():
                # Promo anomaly has an intentional spike; apply wide tolerance
                if scenario_name == "promo_anomaly_240d":
                    if rev < rev_min * 0.3 or rev > rev_max * 4.0:
                        out_of_range.append((str(m), rev))
                else:
                    if rev < rev_min * 0.4 or rev > rev_max * 2.0:
                        out_of_range.append((str(m), rev))

            assert not out_of_range, (
                f"[{scenario_name}] Months with revenue out of range: {out_of_range}"
            )


