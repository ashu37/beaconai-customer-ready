# Test: Cold start has short history
# ---------------------------------------------------------------------------

class TestColdStart:
    def test_cold_start_has_short_history(self, scenarios):
        cfg = scenarios["cold_start_45d"]
        expected_days = cfg["days_history"]

        df = _load_orders("cold_start_45d")
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            dates = pd.to_datetime(df["Created at"], errors="coerce", utc=True).dt.tz_localize(None)

        span = (dates.max() - dates.min()).days
        # Allow up to 5 days of slack
        assert span <= expected_days + 5, (
            f"cold_start_45d spans {span} days, expected <= {expected_days + 5}"
        )

    def test_cold_start_is_shorter_than_healthy(self, scenarios):
        cold = _load_orders("cold_start_45d")
        healthy = _load_orders("healthy_beauty_240d")

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            cold_dates = pd.to_datetime(cold["Created at"], errors="coerce", utc=True).dt.tz_localize(None)
            healthy_dates = pd.to_datetime(healthy["Created at"], errors="coerce", utc=True).dt.tz_localize(None)

        cold_span = (cold_dates.max() - cold_dates.min()).days
        healthy_span = (healthy_dates.max() - healthy_dates.min()).days

        assert cold_span < healthy_span, (
            f"cold_start span ({cold_span}d) should be less than healthy_beauty span ({healthy_span}d)"
        )


